// Context modeling lookup tables for literal encoding (quality >= 5).
//
// Brotli uses four context modes to predict the next literal byte based on
// the two preceding bytes (p1, p2). The context ID feeds into the context
// map, which selects a Huffman tree for coding the next byte.
//
// The four modes are:
//
//   - contextLSB6: context is the low 6 bits of p1 (p2 ignored).
//   - contextMSB6: context is the high 6 bits of p1 (p2 ignored).
//   - contextUTF8: second-order model tuned for UTF-8 text. For ASCII
//     bytes the context encodes character class of p1 (16 classes) and
//     broad class of p2 (4 classes), giving 64 contexts. For multi-byte
//     sequences it distinguishes continuation vs. lead bytes.
//   - contextSigned: second-order model tuned for signed integer data.
//     Similar to MSB6 but with 8 buckets for p1 and 8 for p2.
//
// The context is computed as:
//
//	context = contextLookupTable[mode<<9 + p1] | contextLookupTable[mode<<9 + 256 + p2]
//
// Each mode occupies 512 bytes in the table: 256 for the p1 contribution
// and 256 for the p2 contribution. The result is a 6-bit context ID (0..63).
//
// RFC 7932 Section 7.1.

