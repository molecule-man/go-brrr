func fastLog2(v int) float64 {
	if v < len(log2Table) {
		return log2Table[v]
	}

	return math.Log2(float64(v))
}

// bitsEntropy computes the Shannon entropy of a population histogram,
// returning the minimum number of bits needed to encode the symbols.
// Guarantees at least one bit per symbol.
func bitsEntropy(population []uint32) float64 {
	var sum int
	var retval float64
	for _, p := range population {
		sum += int(p)
		retval -= float64(p) * fastLog2(int(p))
	}
	if sum != 0 {
		retval += float64(sum) * fastLog2(sum)
	}
	if retval < float64(sum) {
		retval = float64(sum)
	}
	return retval
}
